package searcha2dmatrixii

import (
	"testing"

	"github.com/stretchr/testify/assert"
)

func TestSearchMatrix(t *testing.T) {
	cases := []struct {
		matric   [][]int
		target   int
		expected bool
	}{
		{[][]int{
			{1, 4, 7, 11, 15},
			{2, 5, 8, 12, 19},
			{3, 6, 9, 16, 22},
			{10, 13, 14, 17, 24},
			{18, 21, 23, 26, 30},
		}, 5, true},
		{[][]int{
			{1, 4, 7, 11, 15},
			{2, 5, 8, 12, 19},
			{3, 6, 9, 16, 22},
			{10, 13, 14, 17, 24},
			{18, 21, 23, 26, 30},
		}, 20, false},
		{[][]int{
			{93, 157, 226, 308, 365, 384, 479, 539, 557, 652}, {118, 234, 287, 368, 395, 432, 480, 607, 634, 723}, {132, 263, 381, 453, 525, 533, 577, 650, 707, 800}, {171, 307, 473, 504, 526, 596, 643, 719, 776, 842}, {233, 319, 514, 571, 668, 710, 733, 777, 875, 886}, {318, 362, 555, 605, 717, 782, 809, 884, 889, 940}, {349, 415, 622, 708, 787, 795, 824, 921, 957, 1014}, {414, 420, 656, 789, 813, 898, 954, 1052, 1095, 1175}, {430, 477, 705, 863, 961, 991, 1003, 1121, 1190, 1236}, {524, 611, 793, 868, 1027, 1111, 1112, 1123, 1252, 1253},
		}, 430, true},
	}

	for _, tc := range cases {
		t.Run("", func(t *testing.T) {
			assert.Equal(t, tc.expected, searchMatrix(tc.matric, tc.target))
		})
	}
}
